package tutoring;

public class SwitchCase
{
    public static void main(String[] args)
    {
        int x =1;

        switch (x)
        {
            case 1:
                System.out.println("Keisha");
                break;
            case 2:
                System.out.println("Bobby");
                break;
            case 3:
                System.out.println("Arnold");
                break;
            case 4:
                System.out.println("Dora");
                break;
            default:
                System.out.println("There are no students fits in this number");
                break;
        }

    }
}
